--3. 3 most popular products
select p.product_name as product_name,
	sum(o_i.quantity) as total_quantity
	from products p
	join order_items o_i on p.product_id=o_i.product_id
	group by o_i.product_id, p.product_name
	order by total_quantity desc
	limit 3;

--4. Find total revenue generated by each product category

select p.category, sum(p.price*o_i.quantity) as total_revenue
	from products p
	join order_items o_i on p.product_id=o_i.product_id
	group by p.category;

