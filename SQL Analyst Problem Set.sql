--Advanced SQL Interview Problems

--1. Calculate the total lifetime revenue per customer and rank customers by lifetime value.
select 
	c.name as customer_name, 
	sum(o.total_amount) as lifetime_value, 
	rank() over(order by sum(o.total_amount) desc) as rank
	from customers c
	join orders o on c.customer_id=o.customer_id
	group by c.name, c.customer_id;

--2. Find the first order date for each customer.
select c.name as customer_name,
	min(order_date) as first_order_date
	from customers c
	join orders o on c.customer_id=o.customer_id
	group by c.name, c.customer_id;

--3. Find customers who placed more than one order on different days.
select c.name as customer_name,  count(distinct(o.order_date)) as order_count
	from customers c
	join orders o on c.customer_id=o.customer_id
	group by c.name, c.customer_id having count(distinct(o.order_date))>1;
	
--4. Find orders where the order value is greater than that customer's average order value.
select * from
(select order_id, customer_id, total_amount,
	avg(total_amount) over(partition by customer_id) as avg_amount
	from orders) t
	where total_amount>avg_amount;

