--Using max() and group by 
select c.name as customer_name, max(o.order_date) as order_date
	from customers c
	join orders o on c.customer_id=o.customer_id
	group by c.name, c.customer_id
	order by customer_name;

--Using Particioning and row_number()
select customer_name, order_id, order_date from
	(select c.name as customer_name, o.order_id, order_date,
	row_number() over(partition by c.customer_id order by o.order_date desc) as row_num
	from customers c
	join orders o on c.customer_id=o.customer_id)
where row_num=1;


