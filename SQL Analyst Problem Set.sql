--7 Rank products by revenue inside each category
select p.category, p.product_name, sum(p.price*o_i.quantity) as revenue,
	rank() over(partition by p.category order by sum(p.price*o_i.quantity) desc) as rank
	from products p
	join order_items o_i on p.product_id=o_i.product_id
	group by p.category, p.product_name;

